def calculation(number1, number2, operator):
	if operator=='*':
		print(number1 * number2)
	elif operator=='-':
		print(number1-number2)
	elif operator=='+':
		print(number1+number2)
	else:
		print(number1/number2)
calculation(12,13,'*')
